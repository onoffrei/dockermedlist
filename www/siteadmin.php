function footer_ob(){
	$ret = array('success'=>false, 'resp'=>array(),'error'=>'');
	ob_start();
?>
	<script>
		sitemap_update_do = function(){
			cs('sitemap/update').then(
				function(update){
					if ((typeof(update.success) != 'undefined') && (update.success == true)){
						alert('Sitemap has been updated successfully')
					}else{
						var error = 'Ceva nu a functionat'
						if ((typeof(update.error) != 'undefined')&&(update.error != '')){
							error = update.error
						}
						alert(error)
					}
				}
			)
		}
	</script>
<?php
	$ret['resp']['html'] = ob_get_contents(); ob_end_clean();
	$ret['success'] = true;
	return $ret;
}
$GLOBALS['footer_ob'] = footer_ob();
